package main

import "fmt"

func main() {

	var a bool = true
	var b int = 10
	var c string = " loni"
	var d float32 = 123.123

	fmt.Println("Boolean: ", a)
	fmt.Println("Integer: ", b)
	fmt.Println("String : ", c)
	fmt.Println("Float32: ", d)
}

// Output:
/*
Boolean:  true
Integer:  10
String :   loni
Float32:  123.123
*/
